
public class Hello_01 {

	// 시작점 또는 Launcher
	public static void main(String[] args) {
		
		// 큰따옴표로 묶인 내용은 문자열 리터럴 이라고 하며
		// 아래 코드는 문자열 리터럴을 콘솔에 보여라 라는 코드
		System.out.println("Republic of Korea");
		System.out.println("30+40");
		System.out.println("30*40");

		// 문자열 리터럴 Republic of Korea를 덧셈연산하여 
		// 콘솔에 나타내라 
		// 문자열 리터럴 Republic of korea를 연결하여 콘솔에 보여달라
		// 리터럴연산
		
		System.out.println("Republic" + "Of" + "Korea");
		
		// 문자열 리터럴에 빈칸(space)이 포함되면 
		// 눈에 보이지는 않으나 빈칸도 하나의 문자열 리터럴이 된다
		System.out.println("Republic " + "Of " + "Korea");
		
		
		
	}
}
